const { eq } = require("cypress/types/lodash");
var Countly = require("../../lib/countly");
var hp = require("../support/helper");

function initMain(deviceId, offline) {
    Countly.init({
        app_key: "YOUR_APP_KEY",
        url: "https://try.count.ly",
        device_id: deviceId,
        tests: true,
        offline_mode: offline
    });
}
function validateSdkGeneratedId(providedDeviceId) {
    expect(providedDeviceId).to.exist;
    expect(providedDeviceId.length).to.eq(16);
    expect(Countly._internals.isUUID(providedDeviceId)).to.be.ok;
}
function validateInternalDeviceIdType(expectedType) {
    expect(expectedType).to; eq(Countly.getInternalDeviceIdType());
}
